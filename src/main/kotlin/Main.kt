//fungsi utama yang digunakan
fun main() {
    //var merupakan code yang dapat ditimpa oleh code lain atau code yang dapat disetel berulang-ulang
    var myName ="Rahell"
    //sedangkan val merupakan code yang hanya dapat disetel satu kali atau code yang tidak dapat ditimpa oleh kode lain
    val myName1 = "Hudha"
    //print digunakan untuk mencetak output yang telah disediakan atau output yang dibuat dalam fungsi print
    print(myName)
    print(myName1)
    print("Hello world")
}
